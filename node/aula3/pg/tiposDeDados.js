// string usando aspas simples
let nome = 'Diogo';
console.log(nome);

// string usando aspas duplas
let sobrenome = "Thompson";
console.log(sobrenome);

//inteiro
let idade = 35;
console.log(idade);

//decimal
let altura = 1.70;
console.log(altura);

//Booleano true
let diaEstaChuvoso = true;
console.log(diaEstaChuvoso);

//Booleano false
let vouParaPraia = false;
console.log(vouParaPraia);

// Valor Not a Number ou NaN
let naoNumerico = NaN;
console.log(naoNumerico);

// Valor null ou nulo
let jogadores = null;
console.log(jogadores);

// Valor undefined
let espectadores = undefined;
espectadores = 10
console.log(espectadores);

