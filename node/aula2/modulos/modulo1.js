//Esta é minha função
function somaDois(valor1,valor2) {
    //Aqui fica o conteúdo
    const resultado = valor1 + valor2;

    return resultado;
}

//Esta é minha função
function somaTres(valor1,valor2,valor3) {
    //Aqui fica o conteúdo
    const resultado = valor1 + valor2 + valor3;
    
    return resultado;
}

module.exports ={
    somaDois,
    somaTres
}